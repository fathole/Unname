public enum StatType
{
    // --- Core Combat Stats ---
    HP,                 // 氣血 (Health Points)
    MaxHP,              // 最大氣血 (Max Health Points)
    MP,                 // 內力 (Mana Points / Internal Energy)
    MaxMP,              // 最大內力 (Max Mana Points)
    PhysicalAttack,     // 外功 (Physical Attack)
    PhysicalDefense,    // 外防 (Physical Defense)
    InternalAttack,     // 內功 (Internal Attack)
    InternalDefense,    // 內防 (Internal Defense)
    Speed,              // 身法 (Speed)
    Accuracy,           // 命中 (Accuracy)
    Evasion,            // 閃避 (Evasion)

    // --- Fundamental Potential Stats ---
    Strength,           // 力道 (Strength)
    Constitution,       // 根骨 (Constitution)
    Willpower,          // 內勁 (Willpower / Spirit)
    Intelligence,       // 悟性 (Intelligence / Comprehension)
    Dexterity,          // 靈巧 (Dexterity)

    // --- Tactical Movement Stats ---
    MoveRange,          // 移動範圍 (Move Range)
    JumpHeight          // 跳躍高度 (Jump Height)
}